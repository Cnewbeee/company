<template>
  <div class="app-container">
    <el-form :model="queryParams" ref="queryForm" size="small" :inline="true" v-show="showSearch" label-width="68px">
      <el-form-item label="职位编号" prop="posId">
        <el-input
          v-model="queryParams.posId"
          placeholder="请输入职位编号"
          clearable
          @keyup.enter.native="handleQuery"
        />
      </el-form-item>
      <el-form-item label="基本工资" prop="basicSalary">
        <el-input
          v-model="queryParams.basicSalary"
          placeholder="请输入基本工资"
          clearable
          @keyup.enter.native="handleQuery"
        />
      </el-form-item>
      <el-form-item label="薪资等级" prop="salaryLevel">
        <el-input
          v-model="queryParams.salaryLevel"
          placeholder="请输入薪资等级"
          clearable
          @keyup.enter.native="handleQuery"
        />
      </el-form-item>
      <el-form-item label="职务补贴" prop="positionAllowance">
        <el-input
          v-model="queryParams.positionAllowance"
          placeholder="请输入职务补贴"
          clearable
          @keyup.enter.native="handleQuery"
        />
      </el-form-item>
      <el-form-item>
        <el-button type="primary" icon="el-icon-search" size="mini" @click="handleQuery">搜索</el-button>
        <el-button icon="el-icon-refresh" size="mini" @click="resetQuery">重置</el-button>
      </el-form-item>
    </el-form>

    <el-row :gutter="10" class="mb8">
      <el-col :span="1.5">
        <el-button
          type="primary"
          plain
          icon="el-icon-plus"
          size="mini"
          @click="handleAdd"
          v-hasPermi="['salarystandard:salarystandard:add']"
        >新增</el-button>
      </el-col>
      <el-col :span="1.5">
        <el-button
          type="success"
          plain
          icon="el-icon-edit"
          size="mini"
          :disabled="single"
          @click="handleUpdate"
          v-hasPermi="['salarystandard:salarystandard:edit']"
        >修改</el-button>
      </el-col>
      <el-col :span="1.5">
        <el-button
          type="danger"
          plain
          icon="el-icon-delete"
          size="mini"
          :disabled="multiple"
          @click="handleDelete"
          v-hasPermi="['salarystandard:salarystandard:remove']"
        >删除</el-button>
      </el-col>
      <el-col :span="1.5">
        <el-button
          type="warning"
          plain
          icon="el-icon-download"
          size="mini"
          @click="handleExport"
          v-hasPermi="['salarystandard:salarystandard:export']"
        >导出</el-button>
      </el-col>
      <right-toolbar :showSearch.sync="showSearch" @queryTable="getList"></right-toolbar>
    </el-row>

    <el-table v-loading="loading" :data="salarystandardList" @selection-change="handleSelectionChange">
      <el-table-column type="selection" width="55" align="center" />
  <el-table-column label="职位名称" align="center" prop="posName" />
      <el-table-column label="基本工资" align="center" prop="basicSalary" />
      <el-table-column label="薪资等级" align="center" prop="salaryLevel" />
      <el-table-column label="职务补贴" align="center" prop="positionAllowance" />
      <el-table-column label="操作" align="center" class-name="small-padding fixed-width">
        <template slot-scope="scope">
          <el-button
            size="mini"
            type="text"
            icon="el-icon-edit"
            @click="handleUpdate(scope.row)"
            v-hasPermi="['salarystandard:salarystandard:edit']"
          >修改</el-button>
          <el-button
            size="mini"
            type="text"
            icon="el-icon-delete"
            @click="handleDelete(scope.row)"
            v-hasPermi="['salarystandard:salarystandard:remove']"
          >删除</el-button>
        </template>
      </el-table-column>
    </el-table>
    
    <pagination
      v-show="total>0"
      :total="total"
      :page.sync="queryParams.pageNum"
      :limit.sync="queryParams.pageSize"
      @pagination="getList"
    />

    <!-- 添加或修改工资标准对话框 -->
    <el-dialog :title="title" :visible.sync="open" width="500px" append-to-body>
      <el-form ref="form" :model="form" :rules="rules" label-width="80px">
          <el-form-item label="职位名称" prop="posId">
            <el-select v-model="form.posId" placeholder="请选择职位名称" filterable>
              <el-option
                v-for="item in jobpositionOptions"
                :key="item.posId"
                :label="item.posName"
                :value="item.posId"
              />
            </el-select>
          </el-form-item>
          <el-form-item label="基本工资" prop="basicSalary">
            <el-input v-model="form.basicSalary" placeholder="请输入基本工资" />
          </el-form-item>
          <el-form-item label="薪资等级" prop="salaryLevel">
            <el-input v-model="form.salaryLevel" placeholder="请输入薪资等级" />
          </el-form-item>
          <el-form-item label="职务补贴" prop="positionAllowance">
            <el-input v-model="form.positionAllowance" placeholder="请输入职务补贴" />
          </el-form-item>
      </el-form>
      <div slot="footer" class="dialog-footer">
        <el-button type="primary" @click="submitForm">确 定</el-button>
        <el-button @click="cancel">取 消</el-button>
      </div>
    </el-dialog>
  </div>
</template>

<script>
import { listSalarystandard, getSalarystandard, delSalarystandard, addSalarystandard, updateSalarystandard } from "@/api/salarystandard/salarystandard"
import { listJobposition } from "@/api/jobposition/jobposition"

export default {
  name: "Salarystandard",
  data() {
    return {
      // 遮罩层
      loading: true,
      // 选中数组
      ids: [],
      // 非单个禁用
      single: true,
      // 非多个禁用
      multiple: true,
      // 显示搜索条件
      showSearch: true,
      // 总条数
      total: 0,
      // 工资标准表格数据
      salarystandardList: [],
      // 弹出层标题
      title: "",
      // 是否显示弹出层
      open: false,
      // 查询参数
      queryParams: {
        pageNum: 1,
        pageSize: 10,
        posId: null,
        basicSalary: null,
        salaryLevel: null,
        positionAllowance: null
      },
      // 表单参数
      form: {},
      // 表单校验
      rules: {
        basicSalary: [
          { required: true, message: "基本工资不能为空", trigger: "blur" }
        ],
          posId: [
            { required: true, message: "职位名称不能为空", trigger: "change" }
          ],
      }
        ,
        // 职位下拉选项
        jobpositionOptions: []
    }
  },
  created() {
    this.getList()
    this.getJobpositionOptions()
  },
  methods: {
      /** 获取职位列表 */
      getJobpositionOptions() {
        listJobposition({}).then(res => {
          if (res && res.rows) {
            this.jobpositionOptions = res.rows
          }
        })
      },
    /** 查询工资标准列表 */
    getList() {
      this.loading = true
      const doMap = (rows) => {
        this.salarystandardList = rows.map(item => {
          let job = this.jobpositionOptions.find(j => String(j.posId) === String(item.posId))
          return {
            ...item,
            posName: job ? job.posName : (item.posId || "")
          }
        })
      }
      listSalarystandard(this.queryParams).then(response => {
        // 如果职位列表已加载，直接映射
        if (this.jobpositionOptions && this.jobpositionOptions.length > 0) {
          doMap(response.rows)
          this.total = response.total
          this.loading = false
        } else {
          // 职位列表未加载，先加载后再映射
          listJobposition({}).then(res => {
            if (res && res.rows) {
              this.jobpositionOptions = res.rows
            }
            doMap(response.rows)
            this.total = response.total
            this.loading = false
          })
        }
      })
    },
    // 取消按钮
    cancel() {
      this.open = false
      this.reset()
    },
    // 表单重置
    reset() {
      this.form = {
        posId: null,
        basicSalary: null,
        salaryLevel: null,
        positionAllowance: null
      }
      this.resetForm("form")
    },
    /** 搜索按钮操作 */
    handleQuery() {
      this.queryParams.pageNum = 1
      this.getList()
    },
    /** 重置按钮操作 */
    resetQuery() {
      this.resetForm("queryForm")
      this.handleQuery()
    },
    // 多选框选中数据
    handleSelectionChange(selection) {
      this.ids = selection.map(item => item.posId)
      this.single = selection.length!==1
      this.multiple = !selection.length
    },
    /** 新增按钮操作 */
    handleAdd() {
      this.reset()
      this.open = true
      this.title = "添加工资标准"
    },
    /** 修改按钮操作 */
    handleUpdate(row) {
      this.reset()
      const posId = row.posId || this.ids
      getSalarystandard(posId).then(response => {
        this.form = response.data
        this.open = true
        this.title = "修改工资标准"
      })
    },
    /** 提交按钮 */
    submitForm() {
      this.$refs["form"].validate(valid => {
        if (valid) {
          if (this.form.salaryId != null) {
            updateSalarystandard(this.form).then(response => {
              this.$modal.msgSuccess("修改成功")
              this.open = false
              this.getList()
            })
          } else {
            addSalarystandard(this.form).then(response => {
              this.$modal.msgSuccess("新增成功")
              this.open = false
              this.getList()
            })
          }
        }
      })
    },
    /** 删除按钮操作 */
    handleDelete(row) {
      const posIds = row.posId || this.ids
      this.$modal.confirm('是否确认删除工资标准编号为"' + posIds + '"的数据项？').then(function() {
        return delSalarystandard(posIds)
      }).then(() => {
        this.getList()
        this.$modal.msgSuccess("删除成功")
      }).catch(() => {})
    },
    /** 导出按钮操作 */
    handleExport() {
      this.download('salarystandard/salarystandard/export', {
        ...this.queryParams
      }, `salarystandard_${new Date().getTime()}.xlsx`)
    }
  }
}
</script>
